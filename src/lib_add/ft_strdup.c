#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*str;

	len = ft_strlen(s1);
	str = malloc(++len);
	if (!str)
		return (NULL);
	ft_memcpy(str, s1, len);
	return (str);
}
